    void llenarT() {
        dtm.addRow(new Object[]{
            Tcliente_id.getText(),
            Tnombre.getText(),
            Temail.getText(),
            Tdireccion.getText(),
            Ttelefono.getText(),
            "Activo"

        });
    }

    void llenarClienteT(String[] cl) {
        dtm.getDataVector().removeAllElements();
        dtm.addRow(new Object[]{cl[0], cl[1], cl[2], cl[3], cl[4], cl[5]});

    }

    void llenarTabla(ResultSet tabla) {
        dtm.getDataVector().removeAllElements();
        try {
            while (tabla.next()) {
                dtm.addRow(new Object[]{
                    tabla.getString(1),
                    tabla.getString(5),
                    tabla.getString(3),
                    tabla.getString(4),
                    tabla.getString(2),
                    tabla.getString(6)
                });
            }

        } catch (SQLException ex) {
            Logger.getLogger(PgestionarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

