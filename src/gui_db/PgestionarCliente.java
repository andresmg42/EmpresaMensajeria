    private void formMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseClicked
        if (!Tabla.getBounds().contains(evt.getPoint())) {
            Tabla.clearSelection();
        }
    }//GEN-LAST:event_formMouseClicked

    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        if (Tabla.getSelectedRow() != -1) {
            int row = Tabla.getSelectedRow();
            Tcliente_id.setText(String.valueOf(Tabla.getValueAt(row, 0)));
            Tnombre.setText(String.valueOf(Tabla.getValueAt(row, 1)));
            Temail.setText(String.valueOf(Tabla.getValueAt(row, 2)));
            Tdireccion.setText(String.valueOf(Tabla.getValueAt(row, 3)));
            Ttelefono.setText(String.valueOf(Tabla.getValueAt(row, 4)));

        }
    }//GEN-LAST:event_TablaMouseClicked

