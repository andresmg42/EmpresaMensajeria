    private void BguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BguardarActionPerformed

        
        int resultado = cont.insertarCliente(Tcliente_id.getText(), Ttelefono.getText(), Temail.getText(), Tdireccion.getText(), Tnombre.getText(), "Activo");
        if (resultado != -1) {
            JOptionPane.showMessageDialog(this,
                    "El cliente se guardo con exito",
                    "Aviso",
                    JOptionPane.INFORMATION_MESSAGE);
            llenarT();
        } else {
            JOptionPane.showMessageDialog(this,
                    "El cliente no se pudo guardar",
                    "Aviso",
                    JOptionPane.INFORMATION_MESSAGE);
        }
       
    }//GEN-LAST:event_BguardarActionPerformed

