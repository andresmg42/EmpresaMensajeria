    private void BbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BbuscarActionPerformed
        String[] cliente = cont.consultarCliente(Tcliente_id.getText());
        if (cliente[0] != null) {
            llenarClienteT(cliente);
        } else {
            JOptionPane.showMessageDialog(this,
                    "cliente no encontrado",
                    "Aviso",
                    JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_BbuscarActionPerformed

    private void BmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BmodificarActionPerformed
        int resultado = cont.modificarCliente(Tcliente_id.getText(), Ttelefono.getText(), Temail.getText(), Tdireccion.getText(), Tnombre.getText());
        if (resultado != -1) {
            JOptionPane.showMessageDialog(this,
                    "El cliente se modifico con exito",
                    "Aviso",
                    JOptionPane.INFORMATION_MESSAGE);
            llenarClienteT(cont.consultarCliente(Tcliente_id.getText()));
        } else {
            JOptionPane.showMessageDialog(this,
                    "El cliente no se pudo modificar",
                    "Aviso",
                    JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_BmodificarActionPerformed

